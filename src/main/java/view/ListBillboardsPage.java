    public void showTable() {
        model.setRowCount(0); //reset content in table to 0
        for (Billboard b : listBillboard) {
            model.addRow(new Object[]{
                b.getBillboardId(), b.getUserId(), b.getMessage(), b.getInformation(), b.getBackgroundColor(), b.getFontColor(), b.getCreatedAt(), b.getUpdateAt(), b.getPicture()
            });
            
            
        }
    }

