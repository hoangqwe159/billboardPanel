    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String message, information, backgroundColor, fontColor, picture;        
        boolean isOk = true;
        int id = 0;
        
        message = txtMessage.getText();
        information = txtInformation.getText();
        backgroundColor = txtBgColor.getText();
        fontColor = txtFontColor.getText();
        picture = txtPicture.getText();
        
        if(isOk) {
            id = id + 1;
            Date date = new Date();
            Billboard billboard = new Billboard( message, information, backgroundColor, fontColor, id, id, date, date, picture);
            addBillboard(billboard);
            JOptionPane.showMessageDialog(rootPanel, "Add successfully");
            
            txtMessage.setText("");
            txtInformation.setText("");
            txtBgColor.setText("");
            txtFontColor.setText("");
            txtPicture.setText("");
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

