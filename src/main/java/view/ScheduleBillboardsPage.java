    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int scheduleId , timeLimit, nextRun ; 
        String startTime;
        boolean isOk = true;
       
        scheduleId = Integer.parseInt(txtScheduleId.getText());
        timeLimit = Integer.parseInt(txtTimeLimit.getText());
        nextRun = Integer.parseInt(txtNextRun.getText());        
        startTime = txtStartTime.getText();
       
              
        if (txtScheduleId.getText().equals("")){
            isOk = false;
        }

        if(isOk) {            
            Date updateAt = new Date();
            for (Schedule s : listSchedule){
                if (s.getScheduleId() == scheduleId){
                    System.out.println(""+ scheduleId);
                    s.setUpdateAt(updateAt);
                    s.setTimeLimit(timeLimit);
                    s.setNextRun(nextRun);
                    s.setStartTime(startTime);
                }
            }
            
            JOptionPane.showMessageDialog(rootPanel, "Edit successfully");
            showSchedule();
            inputPanel.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(rootPanel, "Choose schedule to edit");
        }

    }//GEN-LAST:event_btnSaveActionPerformed

